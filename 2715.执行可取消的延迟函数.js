/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function (fn, args, t) {
  const time = setTimeout(() => ({ time: t, returned: fn(...args) }), t);

  return () => {
    clearTimeout(time);
    return [];
  };
};

